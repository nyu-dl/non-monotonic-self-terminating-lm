    def st_softmax(
        self, 
        logits: torch.LongTensor, 
        eos_token_id: int, 
        p_eos_prev: Optional[torch.FloatTensor] = None,
        return_p_eos: bool = False,
    ):
        """Non-monotonic self-terminating softmax.
        :param torch.LongTensor logits: [batch_size, dec_seq_len, vocab_size (default = 50257)]
        :param int eos_token_id: eos token id (default = 50256)
        :param torch.FloatTensor p_eos_prev: [batch_size, 1, 1]
        :param bool return_p_eos: for decoding.
        """
        left_v_logits = logits[:, :, :eos_token_id]
        right_v_logits = logits[:, :, eos_token_id+1:]
        v_logits = torch.cat([left_v_logits, right_v_logits], dim=-1)
        eos_logits = logits[:, :, eos_token_id].unsqueeze(dim=-1)

        v_lprobs = v_logits.log_softmax(-1) # (B,T,V-1) non-eos logits
       
        log_bound = torch.tensor([1 - self.epsilon]).log().item()
        if p_eos_prev is None:
            lower_bound = torch.full_like(eos_logits, log_bound)
            log_lower_bound = lower_bound.cumsum(dim=1)
        else:
            log_lower_bound = torch.full_like(eos_logits, log_bound) + p_eos_prev

        f_lower_bound = 1.0 - log_lower_bound.exp() # (B,T,1)        
        
        """
        z^{<eos>}_t: the <eos> token logit at timestep t
        log_eos_ub_score[:, t, :]: log(σ_t)
        log_eos_lb_score[:, t, :]: log(1-σ_t)
        """
        log_eos_ub_score = F.logsigmoid(eos_logits)
        log_eos_lb_score = F.logsigmoid(-eos_logits)
        
        """v_lprobs
        1 - p = 1 - [(1-(1-ε)^t)(1 - σ) + σ]
              = 1 - (1-(1-ε)^t) + (1-(1-ε)^t)σ - σ = (1-ε)^t + σ - (1-ε)^t)σ - σ
              = (1-ε)^t - σ*(1-ε)^t
              = (1 - σ)(1-ε)^t
        => log(1 - p) = log(1 - σ) + t*log(1-ε)
        """
        v_lprobs = log_lower_bound + log_eos_lb_score + v_lprobs
        
        """log_lb
        log(1-σ(z^{<eos>}_t)*f_low(t))
        """
        log_lb = f_lower_bound.log() + log_eos_lb_score
        """
        convex comb. of the p(eos) lowerbound and p(eos) upperbound in log-space. 
        log(α+β) = log(α) - log( σ( log(α) - log(β) ) ) 
        """
        lprob_eos = log_lb - F.logsigmoid(log_lb - log_eos_ub_score)
        log_ps = torch.cat((v_lprobs, lprob_eos), dim=-1)        # (B,T,K)
        
        """
        renormalize output dist.
        """
        log_ps = log_ps - log_ps.logsumexp(-1, keepdim=True)
        
        if return_p_eos:
            return log_ps, log_lower_bound
        return log_ps

